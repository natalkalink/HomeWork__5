// Задача 8. Користувач загадує число. 
//За 3 спроби комп’ютер намагається вгадати число користувача (використати confirm).

for (let i = 1; i <= 3; i++) {
	let guessComputer = Math.floor(Math.random() * 10) + 1

	if (confirm(`Спроба ${i}: Ваше число ${guessComputer}?`)) {
		alert(`Комп'ютер вгадав ваше число з ${i}-ї спроби!`);
		break;
	}
	if (i === 3) {
		alert(`Комп'ютер не вгадав ваше число за 3 спроби`)
	}
}
